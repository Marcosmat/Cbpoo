package cbpoo;

public class Pessoa {
	
	private String nome;
	private String dataNasc;
	private String cpf;
	private String titAcad;
	private String instVinc;
	private String senha;
	
	//************************************
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	//**********************************
	public String getDataNasc() {
		return dataNasc;
	}
	public void setDataNasc(String dataNasc) {
		this.dataNasc = dataNasc;
	}
	//******************************************
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	//*********************************************
	public String getTitAcad() {
		return titAcad;
	}
	public void setTitAcad(String titAcad) {
		this.titAcad = titAcad;
	}
	//*******************************************
	public String getInstVinc() {
		return instVinc;
	}
	public void setInstVinc(String instVinc) {
		this.instVinc = instVinc;
	}
	//*********************************************
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	//*********************************************
	
	
	
}
